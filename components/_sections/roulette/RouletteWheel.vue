<script>
import sha256 from "sha256";

export default {
  props: {
    isEnoughMoney: {
      type: Boolean,
      default: false,
      required: true
    }
  },
  data() {
    return {
      $inner: $(".inner"),
      $spin: $("#spin"),
      $reset: $("#reset"),
      wheelData: $(".data"),
      $mask: $(".mask"),
      maskDefault: "Place Your Bets",
      timer: 9000,
      red: [32, 19, 21, 25, 34, 27, 36, 30, 23, 5, 16, 1, 14, 9, 18, 7, 12, 3]
    };
  },
  mounted() {
    this.setElements();

    this.$reset.hide();

    this.$mask.text(this.maskDefault);
    // so you can swipe it too
    const that = this;
    const myElement = document.getElementById("plate");
    const mc = new Hammer(myElement);
    mc.on("swipe", function(ev) {
      if (!that.$reset.hasClass("disabled")) {
        if (that.$spin.is(":visible")) {
          that.$spin.click();
        } else {
          that.$reset.click();
        }
      }
    });
  },
  methods: {
    // <! MADE BY ECHO
    randomstring() {
      var text = "";
      var possible =
        "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
      for (var i = 0; i < 16; i++)
        text += possible.charAt(Math.floor(Math.random() * possible.length));
      return text;
    },
    hexdec(hexString) {
      hexString = (hexString + "").replace(/[^a-f0-9]/gi, "");
      return parseInt(hexString, 16);
    },
    // <! MADE BY ECHO
    spin() {
      // get a random number between 0 and 36 and apply it to the nth-child selector
      if (!this.isEnoughMoney) {
        this.$notify({
          type: "error",
          title: `Insufficient balance!`,
          duration: this.notificationDuration
        });
        return;
      }

      // algorithm to get a random number by SHA256 Hash cryptograhic algorithm. \\<<!!!->>
      const sha256_algorithm = sha256(
        new Date().getTime() + "-" + this.randomstring()
      );
      const randomNumber = this.hexdec(sha256_algorithm.substr(0, 8)) % 38;
      // <!@#!#--->

      const tempRandom = randomNumber >= 37 ? 37 : randomNumber;
      let color = null;
      if (!navigator.userAgent.includes("Edge")) {
        this.$inner
          .attr("data-spinto", tempRandom)
          .find("li:nth-child(" + tempRandom + ") input")
          .prop("checked", "checked");
      }
      // prevent repeated clicks on the spin button by hiding it
      this.$spin.hide();
      // disable the reset button until the ball has stopped spinning
      this.$reset
        .addClass("disabled")
        .prop("disabled", "disabled")
        .show();

      $(".placeholder").remove();
      this.$emit("changeCanBet", false);
      this.$mask.text("No More Bets");

      // setTimeout(() => {
      //   this.$emit("changeCanBet", false);
      //   this.$mask.text("No More Bets");
      // }, this.timer / 2);

      setTimeout(() => {
        if (navigator.userAgent.includes("Edge")) {
          this.$inner
            .attr("data-spinto", tempRandom)
            .find("li:nth-child(" + tempRandom + ") input")
            .prop("checked", "checked");
        }
        this.$mask.text(this.maskDefault);
      }, this.timer + 500);

      // remove the disabled attribute when the ball has stopped
      setTimeout(() => {
        this.$reset.removeClass("disabled").prop("disabled", "");

        if ($.inArray(tempRandom, this.red) !== -1) {
          color = "red";
        } else {
          color = "black";
        }
        if (tempRandom == 0 || tempRandom == 37) {
          color = "green";
        }

        $(".result-number").text(tempRandom === 37 ? "00" : tempRandom);
        $(".result-color").text(color);
        $(".result").css({ "background-color": "" + color + "" });
        this.wheelData.addClass("reveal");
        this.$inner.addClass("rest");
        this.$emit("placed", randomNumber);
      }, this.timer);
    },
    reset(resetAll = true) {
      this.$emit("changeCanBet", true);
      this.$emit("resetBets");
      this.$inner.attr("data-spinto", "").removeClass("rest");
      this.$reset.hide();
      this.$spin.show();
      this.wheelData.removeClass("reveal");
    },
    setElements() {
      this.$inner = $(".inner");
      this.$spin = $("#spin");
      this.$reset = $("#reset");
      this.wheelData = $(".data");
      this.$mask = $(".mask");
    }
  }
};
</script>

